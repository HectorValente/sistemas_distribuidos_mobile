#Calcule a area de triangulo através da formula (area = base X altura/2)
#Retorne a frase:
#A área do triângulo buscada é

fBase = float(input("Por favor, digite um número para a base do triângulo: "))
fAltura = float(input("Por favor, digite um número para a altura do triângulo: "))

fArea = (fBase * fAltura) / 2
print("A área do triângulo é: ",fArea)